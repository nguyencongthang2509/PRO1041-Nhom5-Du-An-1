    private void Filter(String query) {
        TableRowSorter<DefaultTableModel> tr = new TableRowSorter<DefaultTableModel>(modelHangHoa);
        tblHangHoa.setRowSorter(tr);
        //Check filter query
        if (query != "None") {
            tr.setRowFilter(RowFilter.regexFilter(query));
        } else {
            tblHangHoa.setRowSorter(tr);
        }
    }

